package business.model;

/**
 *
 * @author pablo
 */
public class FinancialOption {
    private final int id;
    private final String name;
    private int value;

    public FinancialOption(int id, String name, int value) {
        this.id = id;
        this.name = name;
        this.value = value;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
